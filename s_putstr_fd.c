#include "libs.h"

void	s_putstr_fd(char *s, int fd)
{
	int	i1;

	if (s)
	{
		i1 = 0;
		while (s[i1])
		{
			s_putchar_fd(s[i1], fd);
			i1++;
		}
	}
}
