#include "libs.h"

char	*s_strdup(const char *s1)
{
	char	*s2;

	s2 = (char *) malloc(s_strlen(s1) + 1);
	if (!s2)
		return (0);
	s_memcpy(s2, s1, s_strlen(s1) + 1);
	return (s2);
}
